public class RxRankPersonalMap {
    private String learningability;
    private String morePeople;
    private int rank;
    private String username;

    public String getLearningability() {
        return learningability;
    }

    public void setLearningability(String learningability) {
        this.learningability = learningability;
    }

    public String getMorePeople() {
        return morePeople;
    }

    public void setMorePeople(String morePeople) {
        this.morePeople = morePeople;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}

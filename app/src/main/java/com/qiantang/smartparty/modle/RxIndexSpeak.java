public class RxIndexSpeak {
    private String img;
    private int number;
    private int dz;
    private String speak_id;
    private String creationtime;
    private String title;
    private int speaktype;
    private String speakurl;

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getDz() {
        return dz;
    }

    public void setDz(int dz) {
        this.dz = dz;
    }

    public String getSpeak_id() {
        return speak_id;
    }

    public void setSpeak_id(String speak_id) {
        this.speak_id = speak_id;
    }

    public String getCreationtime() {
        return creationtime;
    }

    public void setCreationtime(String creationtime) {
        this.creationtime = creationtime;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getSpeaktype() {
        return speaktype;
    }

    public void setSpeaktype(int speaktype) {
        this.speaktype = speaktype;
    }

    public String getSpeakurl() {
        return speakurl;
    }

    public void setSpeakurl(String speakurl) {
        this.speakurl = speakurl;
    }
}
